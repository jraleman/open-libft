#include "libft.h"

/*
** Outputs the string s to the file descriptor fd followed by a ’\n’.
*/

int		ft_putendl_fd(char const *s, int fd)
{
	return (ft_putstr_fd(s, fd) == ft_putchar_fd('\n', fd) ? 0 : -1);
}
